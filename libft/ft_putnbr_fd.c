#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	j;

	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	if (n == 0)
		write(fd, "0", 1);
	if (n < 0 && n != -2147483648)
	{
		write (fd, "-", 1);
		n = -n;
	}
	if (n > 0)
	{
		j = (n % 10) + '0';
		n = n / 10;
		if (n != 0)
		{
			ft_putnbr_fd(n, fd);
		}
		write (fd, &j, 1);
	}
}
